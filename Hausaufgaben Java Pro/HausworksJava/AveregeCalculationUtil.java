package HausworksJava;

public class AveregeCalculationUtil {
    public static double averege(double first, double second, int a, int b) {
        return (first + second) / 2 + (a * b);
    }

    public static double averege(double first, double second, int third, int c, int d) {
        return (first + second + third) / 3 + (c * d);
    }


}
